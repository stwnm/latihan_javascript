// Belajar While (Pengulangan)
var jumlahangkot = 10;
var angkot = 1;
var angkotberoperasi = 6;

while (angkot <= angkotberoperasi) {
  console.log(" Angkott No " + angkot + " beroperasi dengan baik ");
  angkot++;
}

// Belajar For (Pengulangan)
for (angkot = angkotberoperasi + 1; angkot <= jumlahangkot; angkot++) {
  console.log("angkot No " + angkot + " Tidak Beroperasi karna rusak ");
}
